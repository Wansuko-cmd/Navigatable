import annotation.Dynamic
import annotation.Navigatable

@OptIn(ExperimentalMaterial3Api::class)
@Navigatable
@Composable
fun SecondScreen(
    @Dynamic value: String,
    onBackPress: () -> Unit,
) {
    Scaffold(
        topBar = {
            TopAppBar(
                title = { Text(text = "Second Screen") },
                navigationIcon = {
                    IconButton(onClick = onBackPress) {
                        Icon(
                            imageVector = Icons.AutoMirrored.Filled.ArrowBack,
                            contentDescription = null,
                        )
                    }
                }
            )
        }
    ) { innerPadding ->
        Column(
            modifier = Modifier
                .fillMaxSize()
                .padding(innerPadding),
        ) {
            Text(text = value)
        }
    }
}
